def _cap(
    capability_id: str,
    name: str,
    category: str,
    status: str,
    *,
    available_now: bool,
    safe_to_use: bool,
    installed: bool,
    configured: bool,
    verified: bool,
    live: bool,
    evidence_source: str,
    dependencies: list[str] | None = None,
    known_caveats: list[str] | None = None,
    policy_notes: list[str] | None = None,
    fallback_path: str = "manual review",
    next_action: str = "keep current",
    risk_level: str = "low",
    intended_use_case: str = "",
    priority_reason: str = "",
    repo_canon: str = "shay_cli/intelligence_seed.py",
) -> dict[str, Any]:
    if status not in ALLOWED_CAPABILITY_STATUSES:
        raise ValueError(f"unsupported capability status: {status}")
    return {
        "capability_id": capability_id,
        "name": name,
        "category": category,
        "status": status,
        "available_now": available_now,
        "safe_to_use": safe_to_use,
        "installed": installed,
        "configured": configured,
        "verified": verified,
        "live": live,
        "repo_canon": repo_canon,
        "evidence_source": evidence_source,
        "dependencies": dependencies or [],
        "known_caveats": known_caveats or [],
        "policy_notes": policy_notes or [],
        "fallback_path": fallback_path,
        "next_action": next_action,
        "last_verified": LAST_VERIFIED,
        "risk_level": risk_level,
        "intended_use_case": intended_use_case,
        "priority_reason": priority_reason,
    }


